CREATE INDEX contextindex ON usr.PRODUCTS(CATEGORY)
  INDEXTYPE IS CTXSYS.CONTEXT;


DELETE
FROM ORDER_ITEMS WHERE PRODUCT_ID in (
    SELECT P.PRODUCT_ID FROM PRODUCTS P
        JOIN PRODUCT_CATEGORIES PC on P.CATEGORY_ID = PC.CATEGORY_ID
    WHERE contains(P.category, 'Eye WITHIN categoryName') > 0);

DELETE
FROM INVENTORIES WHERE PRODUCT_ID in (
    SELECT P.PRODUCT_ID FROM PRODUCTS P
        JOIN PRODUCT_CATEGORIES PC on P.CATEGORY_ID = PC.CATEGORY_ID
    WHERE contains(P.category, 'Eye WITHIN categoryName') > 0;

DELETE
FROM PRODUCTS WHERE PRODUCT_ID in (
    SELECT P.PRODUCT_ID FROM PRODUCTS P
        JOIN PRODUCT_CATEGORIES PC on P.CATEGORY_ID = PC.CATEGORY_ID
    WHERE contains(P.category, 'Eye WITHIN categoryName') > 0;