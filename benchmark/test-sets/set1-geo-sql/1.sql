SELECT COUNT(*), PC.CATEGORY_NAME
FROM CUSTOMERS
    JOIN COUNTRIES C2 on CUSTOMERS.COUNTRY_ID = C2.COUNTRY_ID
    JOIN ORDERS O on CUSTOMERS.CUSTOMER_ID = O.CUSTOMER_ID
    JOIN ORDER_ITEMS OI on O.ORDER_ID = OI.ORDER_ID
    JOIN PRODUCTS P on OI.PRODUCT_ID = P.PRODUCT_ID JOIN PRODUCT_CATEGORIES PC on P.CATEGORY_ID = PC.CATEGORY_ID
WHERE COUNTRY_NAME like 'Russian Federation'
GROUP BY PC.CATEGORY_NAME;
