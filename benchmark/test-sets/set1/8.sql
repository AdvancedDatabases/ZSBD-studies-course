    SELECT P2.PRODUCT_ID FROM PRODUCTS P2
        JOIN PRODUCT_CATEGORIES PC on P2.CATEGORY_ID = PC.CATEGORY_ID
    WHERE CATEGORY_NAME LIKE 'Eye%');

