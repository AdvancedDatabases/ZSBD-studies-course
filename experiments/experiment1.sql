CREATE TABLE trigger_logs
  (
    log_id NUMBER GENERATED ALWAYS AS IDENTITY(START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    message VARCHAR2( 50 ) NOT NULL
  );

CREATE OR REPLACE TRIGGER TRG_1
AFTER INSERT
    ON trigger_logs
BEGIN
    DBMS_OUTPUT.PUT_LINE('TRIGGER 1 FIRING');
END;

CREATE OR REPLACE TRIGGER TRG_2
AFTER INSERT
    ON trigger_logs FOLLOWS TRG_1
BEGIN
    DBMS_OUTPUT.PUT_LINE('TRIGGER 2 FIRING');
END;

CREATE OR REPLACE TRIGGER TRG_3
AFTER INSERT
    ON trigger_logs FOLLOWS TRG_2
BEGIN
    DBMS_OUTPUT.PUT_LINE('TRIGGER 3 FIRING');
END;

BEGIN
    DBMS_OUTPUT.ENABLE();
END;

